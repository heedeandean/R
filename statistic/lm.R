# 1. 단순회귀모형: x(1개)
x <- c(56,80,50,78,65,75,53,57,53,44)
y <- c(164,180,160,175,170,175,160,169,165,150)
reg <- lm(y~x)
summary(reg)

# 2. 중회귀모형: x(N개)
x1 <- c(4,6,6,7,8,9,9,9,11,12)
x2 <- c(3,4,5,5,6,7,6,8,8,9)
y <- c(38,42,46,47,50,53,52,56,58,62)
reg1 <- lm(y~x1+x2)
summary(reg1)

# 회귀진단: 잔차 분석
# 1. 잔차 산점도: 랜덤처럼 보이는게 좋은 것.
res <- resid(reg1)
plot(fitted(reg1), res) # 예측값, 잔차
abline(0,0)

# 2. 정규확률도
qqnorm(res)
qqline(res)